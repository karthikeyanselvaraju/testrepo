package com.safeway.app.emju.mylist.helper;

public class PDPricing {
	
	private String priceTitleType;
    private String priceTitle;
    private String priceValue;
    
	public String getPriceTitleType() {
		return priceTitleType;
	}
	public void setPriceTitleType(String priceTitleType) {
		this.priceTitleType = priceTitleType;
	}
	public String getPriceTitle() {
		return priceTitle;
	}
	public void setPriceTitle(String priceTitle) {
		this.priceTitle = priceTitle;
	}
	public String getPriceValue() {
		return priceValue;
	}
	public void setPriceValue(String priceValue) {
		this.priceValue = priceValue;
	}
    
}
